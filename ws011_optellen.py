
# Zolang x lager is dan 10 blijven we de ingesprongen
# code herhalen.
# De "while"-loop is van toepassing op de eerstvolgende
# ingesprongen code (indentatie).
# Indentatie is erg belangrijk in Python!
x = 1
while x < 10:
    print(x)
    # Tel 1 op bij x
    x = x + 1

# Kan je de code zo aanpassen dat het volgende uitgeprint wordt:
# 2, 4, 6, 8, 10, 12, 14, 16, 18

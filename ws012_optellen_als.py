# Zolang x lager is dan 10, herhalen we de code
x = 1
while x < 10:
    # Als x gelijk is aan 5 dan voeren we het eerste code blok uit
    if x is 5:
        print("op de helft!")
    else:  # Anders voeren we dit code blok uit
        print(x)
    # En dit wordt altijd in de loop uitgevoerd
    x = x + 1

